package com.facility.FacilityManagement.Modal;

public class User {
	
	private String name;
	private int age;
	private Staff type;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public Staff getType() {
		return type;
	}
	public void setType(Staff type) {
		this.type = type;
	}
	
	

}
